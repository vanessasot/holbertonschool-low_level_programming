#include <stdlib.h>
#include <time.h>
#include <stdio.h>
#include "holberton.h"

/**
 * print_alphabet - Prints the alphabet in lowercase.
 *
 * _putchar - Print the characters.
 *
 * Return: 0 success
 */
void print_alphabet(void)
{
	int i;

	for (i == 97; i <= 122 - 1; i++)
	{
		_putchar(i);
	}
	_putchar('\n');
}
